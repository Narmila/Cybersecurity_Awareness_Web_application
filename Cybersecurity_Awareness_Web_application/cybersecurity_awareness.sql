-- Create schema for the application
CREATE SCHEMA IF NOT EXISTS cybersecurity_awareness;

-- Switch to the new schema
USE cybersecurity_awareness;
-- Create table for storing quiz questions
CREATE TABLE IF NOT EXISTS questions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    question_text TEXT NOT NULL,
    correct_option VARCHAR(255) NOT NULL
);

-- Create table for storing quiz results
CREATE TABLE IF NOT EXISTS quiz_results (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user_name VARCHAR(255) NOT NULL,
    score INT NOT NULL
);

-- Create table for storing contact messages
CREATE TABLE IF NOT EXISTS contact_messages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL,
    message TEXT NOT NULL,
    CONSTRAINT chk_email CHECK (email LIKE '%@%')
);

-- Create table for storing feedback
CREATE TABLE IF NOT EXISTS feedback (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL,
    feedback TEXT NOT NULL,
    CONSTRAINT chk_feedback_length CHECK (CHAR_LENGTH(feedback) > 0)
);
